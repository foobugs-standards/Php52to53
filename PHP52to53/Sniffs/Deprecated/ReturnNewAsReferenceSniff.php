/**
 * This sniff searches for deprecated return values of new assignments.
 *
 * @category  PHP
 * @package   PHP_CodeSniffer
 * @author    René Oelke <rene.oelke@foobugs.com>
 * @copyright 2012 Foobugs
 * @license   BSD Licence
 * @version   $Id$
 * @link      http://pear.php.net/package/PHP_CodeSniffer
 */
class PHP52to53_Sniffs_Deprecated_ReturnNewAsReferenceSniff implements PHP_CodeSniffer_Sniff
{
	/**
	 * A list of tokenizers this sniff supports.
	 *
	 * @var array
	 */
	public $supportedTokenizers = array(
		'PHP',
	);

	/**
	 * Returns the token types that this sniff is interested in.
	 *
	 * @return array(int)
	 * @see PHP_CodeSniffer_Sniff::register()
	 */
	public function register()
	{
		return array(
			T_NEW,
		);
	}

	/**
	 * Processes the tokens that this sniff is interested in.
	 *
	 * @param PHP_CodeSniffer_File $phpcsFile The file where the token was found.
	 * @param int                  $stackPtr  The position in the stack where
	 *                                        the token was found.
	 *
	 * @return void
	 * @see PHP_CodeSniffer_Sniff::process()
	 */
	public function process(PHP_CodeSniffer_File $phpcsFile, $stackPtr)
	{
		// Ignore all but variables
		$tokens = $phpcsFile->getTokens();
		$token = $tokens[$stackPtr]['content'];

		$previousTokenIndex = $phpcsFile->findPrevious(
			PHP_CodeSniffer_Tokens::$emptyTokens,
			($stackPtr - 1),
			null,
			true
		);

		if ('T_BITWISE_AND' === $tokens[$previousTokenIndex]['type']) {
			$warning = sprintf(
				'Assigning the return value of new by reference is deprecated!',
				$token
			);
			$phpcsFile->addWarning($warning, $stackPtr);
		}
	}
}
